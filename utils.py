def letter_grade(n):
	if n>96:
		return "A+"
	if n>93:
		return "A"
	if n>=90:
		return "A-"
	if n>86:
		return "B+"
	if n>83:
		return "B"
	if n>=80:
		return "B-"
	if n>76:
		return "C+"
	if n>73:
		return "C"
	if n>=70:
		return "C-"
	if n>66:
		return "D+"
	if n>63:
		return "D"
	if n>=60:
		return "D-"
	if n>=40:
		return "F+"
	if n>=20:
		return "F"
	return "F-"
